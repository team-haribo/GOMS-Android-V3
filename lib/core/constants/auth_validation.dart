/// 인증 관련 입력값 검증 상수
///
/// 비밀번호 정규식은 회원가입 / 비밀번호 재설정 / 회원탈퇴 등 여러 화면에서
/// 동일하게 사용되므로, 정책이 어긋나지 않도록 이 한 곳에서만 정의한다.
class AuthValidation {
  AuthValidation._();

  /// 비밀번호 정책
  /// - 영문자 최소 1개 포함
  /// - 특수문자(!@#$%^&?*~) 최소 1개 포함 ('+' '_' 등은 의도적으로 제외)
  /// - 영문자 / 숫자 / 위 특수문자만 허용 (숫자는 선택)
  /// - 길이 6~15자
  static final RegExp passwordRegex = RegExp(
    r'^(?=.*[a-zA-Z])(?=.*[!@#$%^&?*~])[a-zA-Z0-9!@#$%^&?*~]{6,15}$',
  );
}
